// print the sum of first n natural numbers

import java.util.*;

public class Loop_For2 {
     
    public static void main( String args[]){
        int sum = 0;
        int n = 4;
        for(int i = 0 ; i<=n ; i++){
            sum = sum + i;
           
        }
        System.out.println(sum);
    }
}
